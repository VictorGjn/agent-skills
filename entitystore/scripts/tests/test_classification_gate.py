FIXTURES = pathlib.Path(__file__).resolve().parent / "fixtures"
CORPUS = FIXTURES / "golden_corpus"

# Hand-counted from golden_seed.json + the manifest above.
CONCEPT_IDS = {
    "concept:atlas-marine-demand-theme", "concept:atlas-marine-stability-theme",
    "concept:borealis-robotics-named-partner-sensing", "concept:borealis-robotics-demand-theme",
    "concept:borealis-robotics-stability-theme", "concept:meridian-foils-named-partner-integration",
    "concept:meridian-foils-demand-theme", "concept:meridian-foils-stability-theme",
    "concept:foil-stability-cluster",
}  # 9 concepts -> public (10th concept below is the override)
CONFIDENTIAL_OVERRIDE_ID = "concept:atlas-marine-named-partner-routing"  # -> confidential
ORG_IDS = {"org:atlas-marine", "org:borealis-robotics", "org:meridian-foils"}  # -> internal (fallback)
PERSON_IDS = {"person:elena-vasko", "person:tomas-reyk", "person:aiko-ferrant"}  # -> restricted

TOTAL = len(CONCEPT_IDS) + 1 + len(ORG_IDS) + len(PERSON_IDS)
assert TOTAL == 16, TOTAL


def _ensure_corpus_built() -> None:
    if (CORPUS / "entities").exists():
        return
    sys.path.insert(0, str(FIXTURES))
    import build_golden_corpus  # noqa: E402
    build_golden_corpus.build(write=True)


class TestClassifyEntity(unittest.TestCase):
    """Pure-function precedence checks — no corpus I/O."""

    def test_no_manifest_fields_fails_closed_to_restricted(self):
        self.assertEqual(
            ce.classify_entity({"id": "concept:x", "kind": "concept"}, {}), "restricted")

    def test_corpus_level_data_classification_is_the_fallback(self):
        self.assertEqual(
            ce.classify_entity({"id": "concept:x", "kind": "concept"},
                               {"data_classification": "internal"}),
            "internal")

    def test_classification_map_overrides_corpus_level(self):
        manifest = {"data_classification": "internal",
                    "classification_map": {"entities/concept/**": "restricted"}}
        self.assertEqual(
            ce.classify_entity({"id": "concept:x", "kind": "concept"}, manifest),
            "restricted")

    def test_longest_most_specific_glob_wins(self):
        manifest = {"data_classification": "internal",
                    "classification_map": {
                        "entities/concept/**": "public",
                        "entities/concept/x.json": "restricted",
                    }}
        self.assertEqual(
            ce.classify_entity({"id": "concept:x", "kind": "concept"}, manifest),
            "restricted")

    def test_no_matching_glob_falls_through_to_corpus_level(self):
        manifest = {"data_classification": "internal",
                    "classification_map": {"entities/person/**": "restricted"}}
        self.assertEqual(
            ce.classify_entity({"id": "org:x", "kind": "org"}, manifest),
            "internal")


