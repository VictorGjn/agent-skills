#!/usr/bin/env python3
"""Classification-cap enforcement gate (M7).

Exercises `classify_entity()` / `_classification_cap()` / `_filter_by_classification()`
directly, then drives wiki_ask / wiki_pack / wiki_audit / stats / resolve end-to-end
against the SAME synthetic fixture used by test_golden_queries.py
(fixtures/golden_corpus/, generated by fixtures/build_golden_corpus.py) — extended
here with fixtures/golden_corpus/manifest.json declaring a mixed classification_map:

    entities/concept/**                                       -> public
    entities/concept/atlas-marine-named-partner-routing.json   -> confidential  (override)
    entities/person/**                                         -> restricted
    (no pattern matches org/*)                                  -> internal (corpus-level default)

That gives every rank in the public<internal<confidential<restricted enum at
least one entity, plus one longest-match-wins override, over a corpus small
enough to hand-count expected withheld_count per cap.

Fully hermetic: no network calls, no real company-brain data — see
fixtures/README.md for the data-governance rationale (this repo, VictorGjn/
agent-skills, is PUBLIC).

Run: python -m pytest entitystore/scripts/tests/test_classification_gate.py -v
"""
